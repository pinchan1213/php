<?php 
// 改行
$br = "<br>";
/*-----------------------------------------
          数学関数 
 ------------------------------------------*/
$num = -9.99;
echo "数字".$num .$br;
/*--------------------------------------------------------
 符号を無視して正の値を返す abs()関数  
 --------------------------------------------------------*/
echo "数字" .abs($num).$br;

/*--------------------------------------------------------
 ceil()関数    0に近づけるよう小数点を切り捨てる
 ---------------------------------------------------------*/
$arg = 5.78;
$val = ceil($arg);
echo $arg.$br.$val.$br;

$arg = 15.05;
$val = ceil($arg);
echo $arg.$br.$val.$br;

$arg = -1.98;
$val = ceil($arg);
echo $arg.$br.$val.$br;

/*----------------------------------------------------------------------  
   floor()関数  小数点を切り捨てる関数
--------------------------------------------------------------------*/
$arg = 15.05;
$val = floor($arg);
echo $arg.$br.$val.$br;

$arg = 4.97;
$val = floor($arg);
echo $arg.$br.$val.$br;

$arg = -7.39;
$val = floor($arg);
echo $arg.$br.$val.$br;

/*--------------------------------------------------------------------------
max()関数 配列内の最大の要素、2つ以上のカンマ区切りの中で最大の要素を返す
------------------------------------------------------------------------*/
$arg = array(23,444,55,6,7,8889);
echo "array =" .max($arg).$br;

/*--------------------------------------------------------------------------
min()関数 最下位の値を取得
------------------------------------------------------------------------*/
$arg = array(1,111,32,41);
$val = min($arg);
echo $val.$br;

/*--------------------------------------------------------
round()関数 四捨五入する
----------------------------------------------------------------*/
$num = 3.4;
$val = round($num);
echo $val.$br;

$num = 3.5;
$val = round($num);
echo $val.$br;
?>